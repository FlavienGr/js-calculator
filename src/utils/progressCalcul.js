const progressCalcul = {
  "+": (firstOperand, spaceCalcul) => firstOperand + spaceCalcul,
  "-": (firstOperand, spaceCalcul) => firstOperand - spaceCalcul,
  "*": (firstOperand, spaceCalcul) => firstOperand * spaceCalcul,
  "/": (firstOperand, spaceCalcul) => firstOperand / spaceCalcul
};

export default progressCalcul;
